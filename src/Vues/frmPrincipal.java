    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        bdd = new ConnexionBDD();
        fm = new FonctionMetier();
        dtmCategories = (DefaultTableModel) tblCategories.getModel();
        dtmJeux = (DefaultTableModel) tblJeux.getModel();
        for(Categorie uneCateg : fm.GetAllCategories())
        {
            v = new Vector();
            v.add(uneCateg.getNumeroCategorie());
            v.add(uneCateg.getNomCategorie());
            dtmCategories.addRow(v);
        }
        tblCategories.setModel(dtmCategories);
        
        txtNumJeux.setText(String.valueOf(fm.GetLastNumJeux()));
    }//GEN-LAST:event_formWindowOpened

    private void tblCategoriesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCategoriesMouseClicked
        // TODO add your handling code here:
        while(tblJeux.getRowCount() != 0)
        {
            dtmJeux.removeRow(0);
        }
        fm = new FonctionMetier();
        for(Jeux unJeux : fm.GetAllJeuxByIdCategories(Integer.parseInt(tblCategories.getValueAt(tblCategories.getSelectedRow(), 0).toString())))
        {
            v = new Vector();
            v.add(unJeux.getNumeroJeux());
            v.add(unJeux.getNomJeux());
            dtmJeux.addRow(v);
        }
        tblJeux.setModel(dtmJeux);
        
    }//GEN-LAST:event_tblCategoriesMouseClicked

