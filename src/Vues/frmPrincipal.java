    private void btnInsererMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnInsererMouseClicked
        // TODO add your handling code here:
        fm = new FonctionMetier();
        int numeroJeux = Integer.parseInt(txtNumJeux.getText());
        
        if(txtNomJeux.getText().isBlank())
        {
            JOptionPane.showMessageDialog(rootPane, "Vous n'avez pas inserer de nom", "Erreur pas de nom", JOptionPane.ERROR_MESSAGE);
        }
        else
        {
            
            String nomJeux = txtNomJeux.getText();
            String nomCateg = cbxCategorie.getSelectedItem().toString();
            fm.AjouterUnJeux(nomCateg, nomJeux, numeroJeux);
            txtNumJeux.setText(String.valueOf(fm.GetLastNumJeux()));
            txtNomJeux.setText("");
        }
        
    }//GEN-LAST:event_btnInsererMouseClicked

